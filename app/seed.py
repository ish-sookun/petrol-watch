def seed_prices():
    """Insert historical STC prices into the database. Skips duplicates."""
    new_count = 0
    for d, mogas, gasoil in HISTORICAL_PRICES:
        existing = db.session.query(FuelPrice).filter_by(date=d).first()
        if not existing:
            db.session.add(FuelPrice(date=d, mogas=mogas, gasoil=gasoil))
            new_count += 1
    db.session.commit()
    return new_count
